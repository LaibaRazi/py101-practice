# Print Your Hobbies - using sep and end

print("My Hobbies:", end="\n\n")

print("1.", "Reading Books", sep=" ", end=" 📚\n")
print("2.", "Photography", sep=" ", end=" 📷\n")
print("3.", "Playing Tennis", sep=" ", end=" 🎾\n")
print("4.", "Video Editing", sep=" ", end=" 🎬\n")
print("5.", "Coding", sep=" ", end=" 💻\n")

print("\nAll hobbies:", end=" ")
print("Reading", "Photography", "Tennis", "Video Editing", "Coding", sep=" | ", end=" ✨\n")
'''

Output:

My Hobbies:

1. Reading Books 📚
2. Photography 📷
3. Playing Tennis 🎾
4. Video Editing 🎬
5. Coding 💻

All hobbies: Reading | Photography | Tennis | Video Editing | Coding ✨
'''